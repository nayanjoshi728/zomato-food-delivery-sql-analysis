-- =========================
-- PROCEDURE: Generate Deliveries
-- (only for orders with status = 'Delivered')
-- =========================

DROP PROCEDURE IF EXISTS generate_deliveries;

DELIMITER $$

CREATE PROCEDURE generate_deliveries()
BEGIN
    DECLARE done  INT DEFAULT 0;
    DECLARE o_id  INT;

    DECLARE cur CURSOR FOR
        SELECT order_id FROM orders WHERE order_status = 'Delivered';

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    OPEN cur;

    delivery_loop: LOOP

        FETCH cur INTO o_id;

        IF done = 1 THEN
            LEAVE delivery_loop;
        END IF;

        INSERT INTO deliveries (
            order_id,
            delivery_status,
            delivery_time,
            rider_id
        )
        VALUES (
            o_id,
            'Delivered',
            SEC_TO_TIME(FLOOR(36000 + (RAND() * 43200))),
            (SELECT rider_id FROM riders ORDER BY RAND() LIMIT 1)
        );

    END LOOP;

    CLOSE cur;
END $$

DELIMITER ;

CALL generate_deliveries();


